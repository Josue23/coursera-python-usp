def main():
    #  a e b  guardam strings
    a = int(input('Digite o primeiro número: '))
    b = int(input("Digite o segundo numero: "))

    #  calcule a soma entre valores que são números inteiros
    soma = a + b

    # imprima a soma
    print('A soma de %d + %d é igual a %d ' % (a, b, soma))

main()
